using i64 = long long;
constexpr int MOD = 1'000'000'007;

void solve() {
  int n;
  std::cin >> n;
  if (n % 2 == 0) {
    std::cout << -1 << '\n';
    return;
  }

  std::vector<int> nums(n);
  std::iota(nums.rbegin(), nums.rend(), 1);

  for (int x : nums) {
    std::cout << x << ' ';
  }
  std::cout << '\n';
}

int main() {
  std::ios_base::sync_with_stdio(false);
  std::cin.tie(nullptr);
  std::cout.tie(nullptr);

  int tc{1};
  std::cin >> tc;

  while (tc--) {
    solve();
  }

  return 0;
}
